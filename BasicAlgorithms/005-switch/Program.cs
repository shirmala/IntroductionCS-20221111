//С клавиатуры вводится число - день недели. Определить день недели

int day=-10;

switch(day)
{
    case >0:
        System.Console.WriteLine("Понедельник");
        break;
    case <0:
        System.Console.WriteLine("Вторник");
        break;
    default:
        System.Console.WriteLine("не правильный ввод");
        break;        
}
